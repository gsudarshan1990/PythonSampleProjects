def cheese_and_crackers(cheese_count,crackers_count):
    print(f'You have {cheese_count} cheese ')
    print(f'you have {crackers_count} crackers')
    print('You can enjoy for the day')
    print('\n')

cheese_and_crackers(20,30)
amount_of_cheese=10
amount_of_crackers=50
cheese_and_crackers(amount_of_cheese,amount_of_crackers)
cheese_and_crackers(10+4,90-50)
cheese_and_crackers(amount_of_cheese+30,amount_of_crackers+10)
