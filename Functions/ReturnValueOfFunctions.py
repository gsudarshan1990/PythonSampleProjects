"""
Example of return value to function
"""
def add(a,b):
    return a+b
def subtract(a,b):
    return a-b
def multiply(a,b):
    return a*b
def divide(a,b):
    return a/b

age = add(30, 5)
height = subtract(78, 4)
weight = multiply(90, 2)
iq = divide(100, 2)

print('age:',age)
print('height:',height)
print('multiply:',weight)
print('quotient',iq)
